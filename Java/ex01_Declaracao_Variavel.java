package Java;
public class ex01_Declaracao_Variavel {
    public static void main(String[] args) {
        //Declaração de Variaveis e Tipagem
        //java (tipagem forte)
        //devemos declarar o tipo da variavel em java, e ela não pode mudar de tipo ao decorrer do programa.

        int numero = 10; //declaração de um valor inteiro
        System.out.println(numero); 

        //quando um valor é do tipo int devemos passar ele como valor númerico, não podemos passar ele por escrito, se queremos escrito temos que fazer utilizando String
    }
}